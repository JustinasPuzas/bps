const MainPage = () => {
  const [name, setName] = useState("");
  const [value, setValue] = useState<number[]>([20, 37]);

  const [eventList, setEventList] = useState([]);

  const handleOnSearch = async () => {
    const axiosUser = await axios.post("/api/event/filter", {
      search: name,
      price1: value[0],
      price2: value[1],
    });
    setEventList(axiosUser.data);
  };

  const handleOnNameChange = (event: any) => {
    setName(event.target.value);
  };

  const handleChange = (event: Event, newValue: number | number[]) => {
    setValue(newValue as number[]);
  };

  function valuetext(value: number) {
    return `${value} Eur`;
  }

  useEffect(() => {
    const getEventList = async () => {
      const axiosUser = await axios.get("/api/event");
      setEventList(axiosUser.data);
    };
    getEventList();
  }, []);

  return (
    <div className={styles.mainPage}>
      <div className={styles.searchBar}>
        <TextField
          onChange={handleOnNameChange}
          id="outlined-basic"
          label="Search"
          variant="outlined"
        />
        <div className={styles.priceContainer}>
          Price: From {value[0]} Eur to {value[1]} Eur
          <Slider
            getAriaLabel={() => "Temperature range"}
            value={value}
            onChange={handleChange}
            valueLabelDisplay="auto"
            getAriaValueText={valuetext}
          />
        </div>
        <Button
          onClick={handleOnSearch}
          variant="outlined"
          endIcon={<SearchIcon />}
        >
          Search
        </Button>
      </div>
      <div className={styles.list}>
        {eventList.map((event: any) => {
          console.log(event);
          return (
            <EventCard
              key={event.id}
              image={event.image}
              name={event.name}
              price={event.price}
              description={event.description}
            />
          );
        })}
      </div>
    </div>
  );
};

const DiscoverBar = () => {
  const [events, setEvents] = useState([] as any[]);

  useEffect(() => {
    const getEventList = async () => {
      const axiosEvents = await axios.get("/api/event");
      const newArr: any[] = [...axiosEvents.data].sort(function () {
        return Math.random() - 0.5;
      });
      setEvents([
        newArr[0],
        newArr[newArr.length - 1],
        newArr[Math.floor(newArr.length / 2)],
      ]);
    };
    getEventList();
  }, []);

  return (
    <div className={styles.discoverBar}>
      <h2>DISCOVER</h2>
      {events.map((event: any) => {
        return (
          <DiscoverCard
            key={event.id}
            image={event.image}
            name={event.name}
            price={event.price}
          />
        );
      })}
    </div>
  );
};

interface EventCardProps {
  key: string;
  image: string;
  name: string;
  price: number;
  description: string;
}

const EventCard = ({
  image,
  name,
  price,
  description,
  key,
}: EventCardProps) => {
  return (
    <div key={key} className={styles.eventCard}>
      <img className={styles.image} src={image}></img>
      <h3>{name}</h3>
      <p>{price} Eur</p>
    </div>
  );
};

interface DiscoverCardProps {
  key: string;
  image: string;
  name: string;
  price: number;
}

const DiscoverCard = ({ key, image, name, price }: DiscoverCardProps) => {
  return (
    <div
      className={styles.discoverCard}
      style={{ backgroundImage: `url(${image})` }}
    >
      <div className={styles.discoverText}>
        <h2>{name}</h2>
        <p>{price} Eur</p>
      </div>
    </div>
  );
};

export default Home;
